package teste;

public class VectorDraw {
	Shape s = new Shape();
	
	int x = s.getX();
	int height = s.getHeight();
		
	public VectorDraw() {
		super();
		this.x = s.getX();
		this.height = s.getHeight();;
	}
	
	public int getX() {
		return this.x;
	}
	public int getHeight() {
		return this.height;
	}
}
